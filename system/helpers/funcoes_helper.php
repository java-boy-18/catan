<?php
function extenso($numero){
  if($numero == 2){
    $extenso = 'dois';
  }elseif($numero == 3){
    $extenso = 'tres';
  }elseif($numero == 4){
    $extenso = 'quatro';
  }elseif($numero == 5){
    $extenso = 'cinco';
  }elseif($numero == 6){
    $extenso = 'seis';
  }elseif($numero == 7){
    $extenso = 'sete';
  }elseif($numero == 8){
    $extenso = 'oito';
  }elseif($numero == 9){
    $extenso = 'nove';
  }elseif($numero == 10){
    $extenso = 'dez';
  }elseif($numero == 11){
    $extenso = 'onze';
  }elseif($numero == 12){
    $extenso = 'doze';
  }
  return $extenso;
}
